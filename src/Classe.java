public class Classe{

    private int babaca;

    public Classe(int babaca) {
        this.babaca = babaca;
    }

    public int getBabaca() {
        return babaca;
    }

    public void setBabaca(int babaca) {
        this.babaca = babaca;
    }

    @Override
    public String toString() {
        return "Classe [babaca=" + babaca + "]";
    }
    

}